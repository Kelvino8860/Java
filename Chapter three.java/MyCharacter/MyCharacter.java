public class MyCharacter
{  private String color;
   private int numberEyes;
   private int numberLimbs;
   private int numberLives;
public  void setColor(String colour)
     {
          color = colour;      
         }
public  void setNumberEyes(int eyes)
     {
            numberEyes = eyes;
         }
public void setNumberLimbs(int limbs)
     {
           numberLimbs = limbs;
         }
public void setNumberLives(int lives)
       {
           numberLives = lives;
             }
public String getColor()
     { 
         return color;
           }
public int getNumberEyes()
     {
       return numberEyes;
       }
public int getNumberLimbs()
      {
        return numberLimbs;
         }
public int getNumberLives()
     {
        return numberLives;
         }
}